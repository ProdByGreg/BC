CREATE DATABASE Escola;
USE Escola;

CREATE TABLE Aluno (
    id_aluno INT PRIMARY KEY,
    nome VARCHAR(100) NOT NULL
);
CREATE TABLE Nota (
    id_nota INT PRIMARY KEY,
    nota DECIMAL(5, 2) NOT NULL,
    id_aluno INT,
    FOREIGN KEY (id_aluno) REFERENCES Aluno(id_aluno)
);

